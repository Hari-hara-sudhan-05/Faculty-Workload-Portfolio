DROP TABLE IF EXISTS Users, Theory, Practical,ProjectCourse,TheoryCourse, PracticalCourse, Project, DepartmentClasses, Dept_duty;

CREATE TABLE Users (
    faculty_id VARCHAR(50) PRIMARY KEY,
    f_name VARCHAR(100),
    password VARCHAR(100),
    entity VARCHAR(100),
    role_in_dept VARCHAR(100),
    CONSTRAINT entity_check CHECK (entity IN ('user', 'admin')),
    CONSTRAINT role_check CHECK (role_in_dept IN ('faculty', 'hod', 'non_teaching_staff'))
);

CREATE TABLE TheoryCourse (
    course_code VARCHAR(10),
    course_name VARCHAR(100),
    hours_per_week INT,
    semester INT,
    sessionn VARCHAR(100),
    CONSTRAINT pk_cccn PRIMARY KEY(course_code,course_name)
);

CREATE TABLE PracticalCourse (
    course_code VARCHAR(10),
    course_name VARCHAR(100),
    hours_per_week INT,
    semester INT,
    sessionn VARCHAR(100),
    PRIMARY key(course_code,course_name)
);

CREATE TABLE ProjectCourse(
    course_code VARCHAR(10),
    course_name VARCHAR(100),
    hours_per_week INT,
    semester INT,
    sessionn VARCHAR(100),
    PRIMARY key(course_code,course_name)
);

CREATE TABLE Theory (
    faculty_id VARCHAR(50),
    course_code VARCHAR(10),
    day_of_the_week VARCHAR(50),
    hour_of_the_week VARCHAR(50),
    handled_duration VARCHAR(50),
    section VARCHAR(100),
    sessionn VARCHAR(100),
    CONSTRAINT fk_courses FOREIGN KEY(course_code) REFERENCES TheoryCourse(course_code) ON DELETE CASCADE,
    CONSTRAINT fk_fid FOREIGN KEY(faculty_id) REFERENCES Users(faculty_id) ON DELETE CASCADE 
);

CREATE TABLE Practical (
    faculty_id VARCHAR(50),
    course_code VARCHAR(10),
    day_of_the_week VARCHAR(50),
    hour_of_the_week_start VARCHAR(50),
    hour_of_the_week_end VARCHAR(45),
    section VARCHAR(50),
    handled_duration VARCHAR(100),
    sessionn VARCHAR(100),
    PRIMARY KEY (faculty_id, course_code, day_of_the_week, hour_of_the_week_start, hour_of_the_week_end),
    CONSTRAINT fk_pcourses FOREIGN KEY(course_code) REFERENCES PracticalCourse(course_code) ON DELETE CASCADE,
    CONSTRAINT fk_pfid FOREIGN KEY(faculty_id) REFERENCES Users(faculty_id) ON DELETE CASCADE
);

CREATE TABLE Project(
    faculty_id VARCHAR(50),
    course_code VARCHAR(10),
    day_of_the_week VARCHAR(50),
    hour_of_the_week_start VARCHAR(50),
    hour_of_the_week_end VARCHAR(50),
    section VARCHAR(50),
    handled_duration VARCHAR(100),
    sessionn VARCHAR(100),
    PRIMARY KEY (faculty_id, course_code, day_of_the_week, hour_of_the_week_start, hour_of_the_week_end),
    CONSTRAINT fk_coursespra FOREIGN KEY(course_code) REFERENCES ProjectCourse(course_code) ON DELETE CASCADE,
    CONSTRAINT fk_profid FOREIGN KEY(faculty_id) REFERENCES Users(faculty_id) ON DELETE CASCADE
);

CREATE TABLE Dept_duty (
    faculty_id VARCHAR(50),
    Responsibilty VARCHAR(100),
    primary key(faculty_id,Responsibilty)
);

